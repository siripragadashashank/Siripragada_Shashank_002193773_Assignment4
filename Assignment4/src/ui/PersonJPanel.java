    public void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) personsJTable.getModel();
        model.setRowCount(0);
        
        for(Person person:house.getPersons()){
            Object[] row = new Object[5];
            row[0]=person;
            row[1]=person.getAge();
            
            model.addRow(row);
        }
    }
